import {
  Box,
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableRow,
  Theme,
  Typography,
} from "@mui/material";
import { makeStyles, styled } from "@mui/styles";
import React, { Fragment, FC } from "react";

import { Entity } from "../../../apiclient/autogenerated";

import { AttributeRow } from "./AttributeRow";

const useStyles = makeStyles<Theme>((theme) => ({
  button: {
    margin: theme.spacing(1),
  },
}));

interface Props {
  attributes: { [key: string]: any }[];
  referralEntities: Entity[];
  setAttributes: (attributes: { [key: string]: any }[]) => void;
}

export const AttributesFields: FC<Props> = ({
  attributes,
  referralEntities,
  setAttributes,
}) => {
  const classes = useStyles();

  return (
    <Box>
      <Box my="32px">
        <Typography variant="h4" align="center">
          属性情報
        </Typography>
      </Box>

      <Table>
        <TableHead>
          <TableRow sx={{ backgroundColor: "#455A64" }}>
            <TableCell sx={{ color: "#FFFFFF" }}>属性名</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>型</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>必須</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>関連削除</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>削除</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>追加</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>ACL設定</TableCell>
          </TableRow>
        </TableHead>
        <TableBody>
          <>
            {attributes.map((attr, index) => (
              <Fragment key={index}>
                {attr.deleted !== true && (
                  <AttributeRow
                    index={index}
                    currentAttr={attr}
                    allAttrs={attributes}
                    referralEntities={referralEntities}
                    setAttributes={setAttributes}
                  />
                )}
              </Fragment>
            ))}
            {attributes.filter((attr) => !attr.deleted).length === 0 && (
              <AttributeRow
                allAttrs={attributes}
                referralEntities={referralEntities}
                setAttributes={setAttributes}
              />
            )}
          </>
        </TableBody>
      </Table>
    </Box>
  );
};
