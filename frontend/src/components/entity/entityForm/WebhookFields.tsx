import AddIcon from "@mui/icons-material/Add";
import DeleteOutlineIcon from "@mui/icons-material/DeleteOutline";
import ModeEditOutlineOutlinedIcon from "@mui/icons-material/ModeEditOutlineOutlined";
import {
  Box,
  Checkbox,
  IconButton,
  Input,
  Modal,
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableRow,
  TextField,
  Theme,
  Typography,
} from "@mui/material";
import React, { FC, Fragment } from "react";
import { makeStyles } from "@mui/styles";

import { Webhook } from "apiclient/autogenerated";
import { WebhookForm } from "components/webhook/WebhookForm";

const useStyles = makeStyles<Theme>((theme) => ({
  button: {
    margin: theme.spacing(1),
  },
  modal: {
    display: "flex",
    alignItems: "center",
    justifyContent: "center",
  },
  paper: {
    backgroundColor: theme.palette.background.paper,
    border: "2px solid #000",
    boxShadow: theme.shadows[5],
    padding: theme.spacing(2, 4, 3),
    width: "50%",
  },
  headerRow: {
    margin: "auto",
  },
  headerInput: {
    margin: "0px 5px 0px 0px",
  },
  tableBorderless: {
    "*" : {
      borderBottom: "none",
    }
  }
}));

interface EditWebhook extends Webhook {
  deleted?: boolean;
}

interface Props {
  entityInfo: {
    name: string;
    note: string;
    isTopLevel: boolean;
    webhooks: EditWebhook[];
    attributes: { [key: string]: any }[];
  };
  setEntityInfo: (entityInfo: {
    name: string;
    note: string;
    isTopLevel: boolean;
    webhooks: EditWebhook[];
    attributes: { [key: string]: any }[];
  }) => void;
}

export const WebhookFields: FC<Props> = ({ entityInfo, setEntityInfo }) => {
  const classes = useStyles();
  const handleChangeWebhook = (index: number, key: string, value: any) => {
    entityInfo.webhooks[index][key] = value;
    setEntityInfo({ ...entityInfo });
  };

  const handleAppendWebhook = (nextTo) => {
    entityInfo.webhooks.splice(nextTo + 1, 0, {
      id: 0,
      url: "",
      label: "",
      isEnabled: false,
      headers: "",
      deleted: false,
    });
    setEntityInfo({ ...entityInfo });
  };

  const handleDeleteWebhook = (index: number) => {
    entityInfo.webhooks[index] = {
      ...entityInfo.webhooks[index],
      deleted: true,
    };
    setEntityInfo({ ...entityInfo });
  };
  console.log("entityInfo", entityInfo);

  const [openModalIndex, setOpenModalIndex] = React.useState(-1);
  const handleOpenModal = (webhookIndex) => {
    setOpenModalIndex(webhookIndex);
  };
  const handleCloseModal = () => setOpenModalIndex(-1);

  return (
    <Box>
      <Box my="32px">
        <Typography variant="h4" align="center">
          Webhook
        </Typography>
      </Box>

      <Table className="table table-bordered">
        <TableHead>
          <TableRow sx={{ backgroundColor: "#455A64" }}>
            <TableCell sx={{ color: "#FFFFFF" }}>URL</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>オプション</TableCell>
            <TableCell />
            <TableCell sx={{ color: "#FFFFFF" }}>URL有効化</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>削除</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>追加</TableCell>
          </TableRow>
        </TableHead>
        <TableBody>
          {entityInfo.webhooks.map((webhook, index) => (
            <TableRow key={index}>
              {/* TODO show isAvailable ??? */}
              {/* TODO update webhook */}
              {webhook.deleted !== true && (
                <Fragment key={index}>
                  <TableCell>
                    <Input
                      type="text"
                      value={webhook.url}
                      placeholder="URL"
                      sx={{ width: "100%" }}
                      onChange={(e) =>
                        handleChangeWebhook(index, "url", e.target.value)
                      }
                    />
                  </TableCell>
                  <TableCell>
                    <Input
                      type="text"
                      value={webhook.label}
                      placeholder="Label"
                      sx={{ width: "100%" }}
                      onChange={(e) =>
                        handleChangeWebhook(index, "label", e.target.value)
                      }
                    />
                  </TableCell>
                  <TableCell>
                    <IconButton onClick={() => handleOpenModal(index)}>
                      <ModeEditOutlineOutlinedIcon />
                    </IconButton>
                  </TableCell>
                  <TableCell>
                    <Checkbox
                      checked={webhook.isEnabled}
                      onChange={(e) =>
                        handleChangeWebhook(index, "enabled", e.target.checked)
                      }
                    />
                  </TableCell>
                  <TableCell>
                    <IconButton onClick={() => handleDeleteWebhook(index)}>
                      <DeleteOutlineIcon />
                    </IconButton>
                  </TableCell>
                  <TableCell>
                    <IconButton onClick={() => handleAppendWebhook(index)}>
                      <AddIcon />
                    </IconButton>
                  </TableCell>
                </Fragment>
              )}
            </TableRow>
          ))}
          {entityInfo.webhooks.filter((webhook) => !webhook.deleted).length ===
            0 && (
            <TableRow>
              <TableCell></TableCell>
              <TableCell></TableCell>
              <TableCell></TableCell>
              <TableCell></TableCell>
              <TableCell></TableCell>
              <TableCell>
                <IconButton onClick={() => handleAppendWebhook(0)}>
                  <AddIcon />
                </IconButton>
              </TableCell>
            </TableRow>
          )}
        </TableBody>
      </Table>
      <Modal
        aria-labelledby="transition-modal-title"
        aria-describedby="transition-modal-description"
        className={classes.modal}
        open={openModalIndex >= 0}
        onClose={handleCloseModal}
      >
        <Box className={classes.paper}>
          hoge( { openModalIndex } )
          <Typography variant={"h6"}>
            AdditionalHeader (Optional)
          </Typography>
          <Typography variant={"caption"}>
            指定した endpoint URL に送るリクエストに付加するヘッダ情報を入力してください。
          </Typography>
          {/*
          <Box className={classes.headerRow}>
            <Box className={classes.headerInput}>
              <TextField label="Key" variant="standard" />
            </Box>
            <Box className={classes.headerInput}>
              <TextField label="Value" variant="standard" />
            </Box>
            <IconButton onClick={() => 1}>
              <DeleteOutlineIcon />
            </IconButton>
            <IconButton onClick={() => 1}>
              <AddIcon />
            </IconButton>
          </Box>
          */}
          <Table className="table">
            <TableBody>
              <TableRow>
                <TableCell sx={{ p: "4px 8px 0px 0px", borderBottom: "0px" }} >
                  <TextField label="Key" variant="standard" fullWidth={true} />
                </TableCell>
                <TableCell sx={{ p: "4px 8px 0px 0px", borderBottom: "0px" }} >
                  <TextField label="Value" variant="standard" fullWidth={true} />
                </TableCell>

                <TableCell sx={{ borderBottom: "0px" }}>
                  <DeleteOutlineIcon />
                </TableCell>

                <TableCell sx={{ borderBottom: "0px" }}>
                  <AddIcon />
                </TableCell>
              </TableRow>
            </TableBody>
          </Table>
        </Box>
      </Modal>
    </Box>
  );
};
