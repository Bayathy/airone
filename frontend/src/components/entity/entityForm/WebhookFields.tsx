import AddIcon from "@mui/icons-material/Add";
import DeleteOutlineIcon from "@mui/icons-material/DeleteOutline";
import ModeEditOutlineOutlinedIcon from "@mui/icons-material/ModeEditOutlineOutlined";
import {
  Box,
  Checkbox,
  IconButton,
  Input,
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableRow,
  Typography,
} from "@mui/material";
import React, { FC, Fragment } from "react";

import { Webhook } from "apiclient/autogenerated";

interface EditWebhook extends Webhook {
  deleted?: boolean;
}

interface Props {
  entityInfo: {
    name: string;
    note: string;
    isTopLevel: boolean;
    webhooks: EditWebhook[];
    attributes: { [key: string]: any }[];
  };
  setEntityInfo: (entityInfo: {
    name: string;
    note: string;
    isTopLevel: boolean;
    webhooks: EditWebhook[];
    attributes: { [key: string]: any }[];
  }) => void;
}

export const WebhookFields: FC<Props> = ({ entityInfo, setEntityInfo }) => {
  const handleChangeWebhook = (index: number, key: string, value: any) => {
    entityInfo.webhooks[index][key] = value;
    setEntityInfo({ ...entityInfo });
  };

  const handleAppendWebhook = (nextTo) => {
    entityInfo.webhooks.splice(nextTo + 1, 0, {
      id: 0,
      url: "",
      label: "",
      isEnabled: false,
      headers: "",
      deleted: false,
    });
    setEntityInfo({ ...entityInfo });
  };

  const handleDeleteWebhook = (index: number) => {
    entityInfo.webhooks[index] = {
      ...entityInfo.webhooks[index],
      deleted: true,
    };
    setEntityInfo({ ...entityInfo });
  };
  console.log("entityInfo", entityInfo);

  return (
    <Box>
      <Box my="32px">
        <Typography variant="h4" align="center">
          Webhook
        </Typography>
      </Box>

      <Table className="table table-bordered">
        <TableHead>
          <TableRow sx={{ backgroundColor: "#455A64" }}>
            <TableCell sx={{ color: "#FFFFFF" }}>URL</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>オプション</TableCell>
            <TableCell />
            <TableCell sx={{ color: "#FFFFFF" }}>URL有効化</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>削除</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>追加</TableCell>
          </TableRow>
        </TableHead>
        <TableBody>
          {entityInfo.webhooks.map((webhook, index) => (
            <TableRow key={index}>
              {/* TODO show isAvailable ??? */}
              {/* TODO update webhook */}
              {webhook.deleted !== true && (
                <Fragment key={index}>
                  <TableCell>
                    <Input
                      type="text"
                      value={webhook.url}
                      placeholder="URL"
                      sx={{ width: "100%" }}
                      onChange={(e) =>
                        handleChangeWebhook(index, "url", e.target.value)
                      }
                    />
                  </TableCell>
                  <TableCell>
                    <Input
                      type="text"
                      value={webhook.label}
                      placeholder="Label"
                      sx={{ width: "100%" }}
                      onChange={(e) =>
                        handleChangeWebhook(index, "label", e.target.value)
                      }
                    />
                  </TableCell>
                  <TableCell>
                    <IconButton>
                      <ModeEditOutlineOutlinedIcon />
                    </IconButton>
                  </TableCell>
                  <TableCell>
                    <Checkbox
                      checked={webhook.isEnabled}
                      onChange={(e) =>
                        handleChangeWebhook(index, "enabled", e.target.checked)
                      }
                    />
                  </TableCell>
                  <TableCell>
                    <IconButton onClick={() => handleDeleteWebhook(index)}>
                      <DeleteOutlineIcon />
                    </IconButton>
                  </TableCell>
                  <TableCell>
                    <IconButton onClick={() => handleAppendWebhook(index)}>
                      <AddIcon />
                    </IconButton>
                  </TableCell>
                </Fragment>
              )}
            </TableRow>
          ))}
          {entityInfo.webhooks.filter((webhook) => !webhook.deleted).length ===
            0 && (
            <TableRow>
              <TableCell></TableCell>
              <TableCell></TableCell>
              <TableCell></TableCell>
              <TableCell></TableCell>
              <TableCell></TableCell>
              <TableCell>
                <IconButton onClick={() => handleAppendWebhook(0)}>
                  <AddIcon />
                </IconButton>
              </TableCell>
            </TableRow>
          )}
        </TableBody>
      </Table>
    </Box>
  );
};
