import { Check } from "@mui/icons-material";
import {
  Box,
  Button,
  Divider,
  ListItemIcon,
  Menu,
  MenuItem,
  TextField,
  Typography,
} from "@mui/material";
import { styled } from "@mui/material/styles";
import React, { FC, useState } from "react";

import { AdvancedSearchResultAttrInfoFilterKeyEnum } from "../../apiclient/autogenerated";

import { AttrsFilter } from "./SearchResults";

const StyledTextField = styled(TextField)({
  margin: "8px",
});

const StyledBox = styled(Box)({
  margin: "8px",
});

interface Props {
  attrName: string;
  newAttrsFilter: AttrsFilter;
  anchorElem: HTMLButtonElement | null;
  handleClose: (name: string) => void;
  setNewAttrsFilter: (filter: AttrsFilter) => void;
  handleSelectFilterConditions: (
    attrfilter?: AttrsFilter,
    overwriteEntryName?: string | undefined,
    overwriteReferral?: string | undefined
  ) => void;
}

export const SearchResultControlMenu: FC<Props> = ({
  attrName,
  newAttrsFilter,
  anchorElem,
  handleClose,
  setNewAttrsFilter,
  handleSelectFilterConditions,
}) => {
  const [keywordRequired, setKeywordRequired] = useState(true);

  const handleClick = (key: AdvancedSearchResultAttrInfoFilterKeyEnum) => {
    setKeywordRequired(
      key === AdvancedSearchResultAttrInfoFilterKeyEnum.TEXT_CONTAINED
    );
    setNewAttrsFilter({
      ...newAttrsFilter,
      [attrName]: { ...newAttrsFilter[attrName], filterKey: key },
    });

    switch (key) {
      case AdvancedSearchResultAttrInfoFilterKeyEnum.DUPLICATED:
      case AdvancedSearchResultAttrInfoFilterKeyEnum.EMPTY:
      case AdvancedSearchResultAttrInfoFilterKeyEnum.NON_EMPTY:
        handleSelectFilterConditions({
          ...newAttrsFilter,
          [attrName]: { ...newAttrsFilter[attrName], filterKey: key },
        });

      case AdvancedSearchResultAttrInfoFilterKeyEnum.CLEARED:
        handleSelectFilterConditions({
          ...newAttrsFilter,
          [attrName]: {
            ...newAttrsFilter[attrName],
            filterKey: key,
            keyword: "",
          },
        });
    }
  };

  const handleChangeKeyword = (e: any) => {
    setNewAttrsFilter({
      ...newAttrsFilter,
      [attrName]: {
        ...newAttrsFilter[attrName],
        keyword: e.target.value,
        filterKey: AdvancedSearchResultAttrInfoFilterKeyEnum.TEXT_CONTAINED,
      },
    });
  };

  const handleKeyPressKeyword = (e: any) => {
    if (e.key === "Enter") {
      handleSelectFilterConditions({
        ...newAttrsFilter,
        [attrName]: {
          ...newAttrsFilter[attrName],
          keyword: e.target.value,
          filterKey: AdvancedSearchResultAttrInfoFilterKeyEnum.TEXT_CONTAINED,
        },
      });
    }
  };

  const filterKey =
    newAttrsFilter[attrName].filterKey ??
    AdvancedSearchResultAttrInfoFilterKeyEnum.CLEARED;
  const keyword = newAttrsFilter[attrName].keyword ?? "";

  return (
    <Menu
      id={attrName}
      open={Boolean(anchorElem)}
      onClose={() => handleClose(attrName)}
      anchorEl={anchorElem}
    >
      <StyledBox>
        <Button
          variant="outlined"
          fullWidth
          onClick={() =>
            handleClick(AdvancedSearchResultAttrInfoFilterKeyEnum.CLEARED)
          }
        >
          <Typography>クリア</Typography>
        </Button>
      </StyledBox>
      <Divider />
      <MenuItem
        onClick={() =>
          handleClick(AdvancedSearchResultAttrInfoFilterKeyEnum.EMPTY)
        }
      >
        {filterKey === AdvancedSearchResultAttrInfoFilterKeyEnum.EMPTY && (
          <ListItemIcon>
            <Check />
          </ListItemIcon>
        )}
        <Typography>空白</Typography>
      </MenuItem>
      <MenuItem
        onClick={() =>
          handleClick(AdvancedSearchResultAttrInfoFilterKeyEnum.NON_EMPTY)
        }
      >
        {filterKey === AdvancedSearchResultAttrInfoFilterKeyEnum.NON_EMPTY && (
          <ListItemIcon>
            <Check />
          </ListItemIcon>
        )}
        <Typography>空白ではない</Typography>
      </MenuItem>
      <MenuItem
        onClick={() =>
          handleClick(AdvancedSearchResultAttrInfoFilterKeyEnum.DUPLICATED)
        }
      >
        {filterKey == AdvancedSearchResultAttrInfoFilterKeyEnum.DUPLICATED && (
          <ListItemIcon>
            <Check />
          </ListItemIcon>
        )}
        <Typography>重複</Typography>
      </MenuItem>
      <StyledTextField
        size="small"
        placeholder="次を含むテキスト"
        value={keyword}
        onChange={handleChangeKeyword}
        onKeyPress={handleKeyPressKeyword}
      />
    </Menu>
  );
};
